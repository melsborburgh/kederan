CREATE table KDR_SIGNUPS (
    SIGNUP_ID               NUMBER GENERATED ALWAYS AS IDENTITY,
    SIGNUP_NAME             VARCHAR2(1000) NOT NULL,
    SIGNUP_ADDRESS          VARCHAR2(1000) NOT NULL,
    SIGNUP_ZIPCODE          VARCHAR2(15) NOT NULL,
    SIGNUP_CITY             VARCHAR2(1000) NOT NULL,
    SIGNUP_PHONE            VARCHAR2(30) NOT NULL,
    SIGNUP_BIRTHDAY         DATE NOT NULL,
    SIGNUP_GENDER           VARCHAR2(1) NOT NULL,
    SIGNUP_EMAIL            VARCHAR2(1000) NOT NULL,
    SIGNUP_ALLERGY          VARCHAR2(1000),
    SIGNUP_MEDICAL_PROBLEMS VARCHAR2(1000),
    SIGNUP_VEGA             VARCHAR2(1) NOT NULL,
    SIGNUP_CONTACT_NAME     VARCHAR2(1000),
    SIGNUP_CONTACT_PHONE    VARCHAR2(30),
    SIGNUP_GP_NAME          VARCHAR2(1000),
    SIGNUP_GP_PHONE         VARCHAR2(30),
    SIGNUP_MEDICIN          VARCHAR2(1000),
    SIGNUP_DINER_FRIDAY     VARCHAR2(1) NOT NULL,
    SIGNUP_BUILDUP_FRIDAY   VARCHAR2(5) NOT NULL,
    SIGNUP_TYPE             VARCHAR2(30) NOT NULL,
    SIGNUP_PHOTO_DISCLAIMER VARCHAR2(1) NOT NULL,
    SIGNUP_NEWSLETTER       VARCHAR2(1) NOT NULL,
    SIGNUP_REGULATIONS      VARCHAR2(1) NOT NULL,
    EVENT_ID                NUMBER,
    CHAR_ID                 NUMBER,
    PLAYER_ID               NUMBER,
    COUNTRY_ID              NUMBER,
    constraint  KDR_SIGNUPS_PK primary key (SIGNUP_ID)
)
